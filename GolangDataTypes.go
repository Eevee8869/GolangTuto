package main

import "fmt"

func main() {
	var i int //int = integer 
	var f float64 //float64 = float value like 3.141592653589793
	var b bool //bool holds 2 values only true or false that's it
	var s string //a string is traditionally a sequence of characters for ex "ThisIsAString8292" 
	fmt.Printf("%v %v %v %q\n", i, f, b, s)
}
